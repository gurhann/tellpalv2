create table purchase_event_types (
    code varchar(64) primary key,
    description varchar(255) null,
    is_active boolean not null default true,
    created_at timestamptz not null default now(),
    constraint chk_purchase_event_types_code_not_blank check (btrim(code) <> ''),
    constraint chk_purchase_event_types_description_not_blank check (
        description is null or btrim(description) <> ''
    )
);

create index idx_purchase_event_types_is_active
    on purchase_event_types (is_active, code);

